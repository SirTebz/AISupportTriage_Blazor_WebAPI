using AISupportTriage.BlazorClient.Auth;
using AISupportTriage.BlazorClient.Services;
using Microsoft.AspNetCore.Components.Authorization;
using Microsoft.AspNetCore.Components.Web;
using Microsoft.AspNetCore.Components.WebAssembly.Hosting;

var builder = WebAssemblyHostBuilder.CreateDefault(args);

builder.RootComponents.Add<AISupportTriage.BlazorClient.App>("#app");
builder.RootComponents.Add<HeadOutlet>("head::after");

// ?? HTTP Client ???????????????????????????????????????????????????????????????
var apiBaseUrl = builder.Configuration["ApiBaseUrl"] ?? "https://localhost:7100";

builder.Services.AddScoped(_ => new HttpClient
{
    BaseAddress = new Uri(apiBaseUrl)
});

// ?? Storage (pure JSInterop — no NuGet package) ???????????????????????????????
builder.Services.AddScoped<LocalStorageService>();

// ?? Auth ??????????????????????????????????????????????????????????????????????
builder.Services.AddAuthorizationCore();

builder.Services.AddScoped<CustomAuthStateProvider>();
builder.Services.AddScoped<AuthenticationStateProvider>(sp =>
    sp.GetRequiredService<CustomAuthStateProvider>());

// ?? App Services ??????????????????????????????????????????????????????????????
builder.Services.AddScoped<AuthClientService>();
builder.Services.AddScoped<TicketApiService>();
builder.Services.AddScoped<SignalRService>();

await builder.Build().RunAsync();